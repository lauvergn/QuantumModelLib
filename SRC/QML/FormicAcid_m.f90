!> @brief Function which makes the initialization of the FormicAcid parameters.
!!
!! @param QModel             TYPE(QML_FormicAcid_t):   result derived type in which the parameters are set-up.
!! @param QModel_in          TYPE(QML_Empty_t):  type to transfer ndim, nsurf ...
!! @param nio_param_file     integer:             file unit to read the parameters.
!! @param read_param         logical:             when it is .TRUE., the parameters are read. Otherwise, they are initialized.
  FUNCTION Init_QML_FormicAcid(QModel_in,read_param,nio_param_file) RESULT(QModel)
    USE QDUtil_m,         ONLY : Identity_Mat
    IMPLICIT NONE

    TYPE (QML_FormicAcid_t)                            :: QModel ! RESULT

    TYPE(QML_Empty_t),           intent(in)      :: QModel_in ! variable to transfer info to the init
    integer,                     intent(in)      :: nio_param_file
    logical,                     intent(in)      :: read_param


    !----- for debuging --------------------------------------------------
    character (len=*), parameter :: name_sub='Init_QML_FormicAcid'
    logical, parameter :: debug = .FALSE.
    !logical, parameter :: debug = .TRUE.
    !-----------------------------------------------------------
    IF (debug) THEN
      write(out_unit,*) 'BEGINNING ',name_sub
      flush(out_unit)
    END IF

    QModel%QML_Empty_t = QModel_in

    QModel%nsurf    = 1
    QModel%ndim     = 9
    QModel%pot_name = 'FormicAcid'

    IF (QModel%option < 0 .OR. QModel%option > 2) QModel%option = 1

    IF (debug) write(out_unit,*) 'init Q0 of FormicAcid'
    SELECT CASE (QModel%option)
    CASE (0) ! ref
      QModel%Q0 = QModel%x0
    CASE (1) ! trans
      QModel%Q0 = QModel%x0
    CASE (2) ! cis
      QModel%Q0 = QModel%x0
    CASE Default

      write(out_unit,*) ' ERROR in ',name_sub
      write(out_unit,*) ' This option is not possible. option: ',QModel%option
      write(out_unit,*) ' Its value MUST be 0,1,2'

      STOP
    END SELECT


    IF (debug) write(out_unit,*) 'init d0GGdef of FormicAcid'
    QModel%d0GGdef = Identity_Mat(QModel%ndim)

    IF (QModel%PubliUnit) THEN
      write(out_unit,*) 'PubliUnit=.TRUE.,  Q:[Bohr,Bohr,Rad,Bohr,Rad,Rad], Energy: [Hartree]'
    ELSE
      write(out_unit,*) 'PubliUnit=.FALSE., Q:[Bohr,Bohr,Rad,Bohr,Rad,Rad], Energy: [Hartree]'
    END IF

    IF (debug) THEN
      write(out_unit,*) 'QModel%pot_name: ',QModel%pot_name
      write(out_unit,*) 'END ',name_sub
      flush(out_unit)
    END IF

  END FUNCTION Init_QML_FormicAcid
