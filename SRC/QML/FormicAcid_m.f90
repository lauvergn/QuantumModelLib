!> @brief Subroutine wich calculates the FormicAcid potential with derivatives up to the 2d order.
!!
!! @param QModel             TYPE(QML_FormicAcid_t):    derived type in which the parameters are set-up.
!! @param Mat_OF_PotDia(:,:) TYPE (dnS_t):         derived type with the potential (pot),  the gradient (grad) and the hessian (hess).
!! @param dnQ(:)             TYPE (dnS_t)          value for which the potential is calculated
!! @param nderiv             integer:              it enables to specify up to which derivatives the potential is calculated:
!!                                                 the pot (nderiv=0) or pot+grad (nderiv=1) or pot+grad+hess (nderiv=2).
  SUBROUTINE EvalPot_QML_FormicAcid(QModel,Mat_OF_PotDia,dnQ,nderiv)
    USE ADdnSVM_m
    IMPLICIT NONE

    CLASS(QML_FormicAcid_t),  intent(in)    :: QModel
    TYPE (dnS_t),             intent(inout) :: Mat_OF_PotDia(:,:)
    TYPE (dnS_t),             intent(in)    :: dnQ(:)
    integer,                  intent(in)    :: nderiv

    SELECT CASE (QModel%option)

    CASE (0,1,2)
      CALL EvalPot1_QML_FormicAcid(Mat_OF_PotDia,dnQ,QModel)

    CASE Default
      write(out_unit,*) ' ERROR in EvalPot_QML_FormicAcid '
      write(out_unit,*) ' This option is not possible. option: ',QModel%option
      write(out_unit,*) ' Its value MUST be 1'

      STOP
    END SELECT

  END SUBROUTINE EvalPot_QML_FormicAcid

